                for (int z = 0; z < depth; z++) {
                    if (y + 1 < this.height) {
                        Cells[x, y, z].addNeighbour(Cells[x, y + 1, z]);
                    }
                    if (x + 1 < this.width){
                        Cells[x, y, z].addNeighbour(Cells[x + 1, y, z]);
                    }
                    if (x > 0){
                        Cells[x, y, z].addNeighbour(Cells[x - 1, y, z]);
                    }
                    if (z + 1 < this.depth){
                        Cells[x, y, z].addNeighbour(Cells[x , y, z + 1]);
                    }
                    if (z > 0){
                        Cells[x, y, z].addNeighbour(Cells[x, y, z - 1]);
