        void Update() {

            this.iteration++;
            if (iteration % 10 == 0){  

				for (int x = 0; x < width; x++){
                    for (int y = 0; y < height; y++){
                        for (int z = 0; z < depth; z++){
                            if (Cells[x,y,z].getType() == CellType.BURNING && Cells[x, y, z].getFuel() == CellFuel.AIR){
                                Cells[x, y, z].applyWind();
                            }
                        }
                    }
                }

                for (int x = 0; x < width; x++){
                    for (int y = 0; y < height; y++){
                        for (int z = 0; z < depth; z++){
                            if (Cells[x,y,z].getType() == CellType.BURNING){
                                Cells[x, y, z].fireSpread();
                            }
                        }
                    }
                }

                for (int x = 0; x < width; x++){
                    for (int y = 0; y < height; y++){
                        for (int z = 0; z < depth; z++){
                            // smoke is spreading to other cells from cell on fire, burned one or air with smoke
                            if (Cells[x,y,z].getType() == CellType.BURNING || Cells[x,y,z].getType() == CellType.BURNED || Cells[x,y,z].getSmokeVol() < 100){
                                Cells[x, y, z].smokeSpread();
                            }
                        }
                    }
                }

                for (int x = 0; x < width; x++){
                    for (int y = 0; y < height; y++){
                        for (int z = 0; z < depth; z++){
                            // heat in the ground and convection in the air
                            if (Cells[x,y,z].getFuel() == CellFuel.GROUND || Cells[x, y, z].getFuel() == CellFuel.AIR){
                                Cells[x, y, z].heatSpread();
                            }
                        }
                    }
                }

                for (int x = 0; x < width; x++){
                    for (int y = 0; y < height; y++){
                        for (int z = 0; z < depth; z++){
                            Cells[x, y, z].checkState();
                        }
                    }
                }

                for (int x = 0; x < width; x++){
                    for (int y = 0; y < height; y++){
                        for (int z = 0; z < depth; z++){

                            // make smoke visible (or not)
                            if (Cells[x, y, z].getFuel() == CellFuel.AIR && Cells[x,y,z].getType() != CellType.BURNED){
                                float smokeTransp = (Cells[x,y,z].getSmokeVol()) / 100;
                                CubeArray[x, y, z].GetComponent<MeshRenderer>().material.color =  new Color(0.5f, 0.5f, 0.5f, smokeTransp);
                            }

                            // make fire cells yellow
                            if (Cells[x,y,z].getType()== CellType.BURNING && Cells[x, y, z].getFuel() != CellFuel.AIR){
                               CubeArray[x, y, z].GetComponent<MeshRenderer>().material.color =  new Color(0.7f, 0.18f, 0.1f, 0.78f);
                            }
                            //change color to black if cell is burned or make transparent (if air)
                            if (Cells[x,y,z].getType()== CellType.BURNED){
                                if (Cells[x,y,z].getFuel() == CellFuel.AIR)
                                    CubeArray[x, y, z].GetComponent<MeshRenderer>().material.color =  new Color(0f, 0f, 0f, 0f);
                                else
                                    CubeArray[x, y, z].GetComponent<MeshRenderer>().material.color =  new Color(0f, 0f, 0f, 1f);
                            }   
                        }
                    }
                }
            }
    }


