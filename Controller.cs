    void Start(){
        iteration = 0;
        for (int x = 0; x < width; x++){
            for (int y = 0; y < height; y++){
                for (int z = 0; z < depth; z++){
                    CubeArray[x, y, z] = (GameObject)Instantiate(CubeCreator, new Vector3(x, y + 1, z), transform.rotation);
                }
            }
